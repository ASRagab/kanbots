export function pickSpinnerVerb(seed?: string | number): string {
  if (seed === undefined) {
    return SPINNER_VERBS[Math.floor(Math.random() * SPINNER_VERBS.length)]!;
  }
  // Deterministic pick — useful so each run keeps the same verb across
  // re-renders without storing it in a ref.
  let hash = 0;
  const s = String(seed);
  for (let i = 0; i < s.length; i++) {
    hash = (hash * 31 + s.charCodeAt(i)) | 0;
  }
  return SPINNER_VERBS[Math.abs(hash) % SPINNER_VERBS.length]!;
}

